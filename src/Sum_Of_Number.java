
// adding two number and calculate their sum, a + b = 75,

public class Sum_Of_Number {
    public static void main (String args []) {
        int a, b;
        a = 75;
        b = 21;

        System.out.println (a+b);

        System.out.println (a-b);

        System.out.println (a*b);

        System.out.println (a/b);

        System.out.println (a%b);
    }
}
